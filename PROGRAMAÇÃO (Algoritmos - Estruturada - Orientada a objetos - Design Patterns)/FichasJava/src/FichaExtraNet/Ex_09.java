package FichaExtraNet;

public class Ex_09 {
    public static void main(String[] args) {

        /*
Faça um programa que imprima na tela apenas os números ímpares entre 1 e 50.
*/


        for (int a = 1; a <= 50; a++) {
            if (a % 2 != 0) {
                System.out.println(a);
            }
        }

    }
}

