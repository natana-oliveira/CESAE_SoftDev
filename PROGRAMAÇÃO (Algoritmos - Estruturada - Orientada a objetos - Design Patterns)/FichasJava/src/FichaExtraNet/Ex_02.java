package FichaExtraNet;

public class Ex_02 {
    public static void main(String[] args) {
        /*
        Crie um procedimento que mostre na consola os números ímpares de 1 a 100.
         */

        int i;

        for (i=1; i<=100; i+=2) {
            System.out.println(i);
        }

    }
}
