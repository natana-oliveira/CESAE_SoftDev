package Ex_06;

public class Calculadora {
    public Calculadora() {
    }

    public double somar(double num1, double num2) { //Metodo para somar
        return num1 + num2;
    }

    public double subtrair(double num1, double num2) { //Metodo para subtrair
        return num1 - num2;
    }

    public double multiplicar(double num1, double num2) { //Metodo para multiplicar
        return num1 * num2;
    }

    public double dividir(double num1, double num2) { //Metodo para dividir
        return num1 / num2;
    }


}
